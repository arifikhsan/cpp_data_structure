#include <iostream>
using namespace std;

main()
{
  cout << "2 < 3 = " << (2 < 3) << endl;
  cout << "2 > 3 = " << (2 > 3) << endl;
  cout << "2 == 3 = " << (2 == 3) << endl;
  cout << "2 != 3 = " << (2 != 3) << endl;
  cout << "2 == 2 = " << (2 == 2) << endl;
  cout << "2 != 2 = " << (2 != 2) << endl;
}
